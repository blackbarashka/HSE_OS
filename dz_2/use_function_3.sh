#!/bin/bash
# Этот скрипт демонстрирует использование функций.
# Функция greet выводит приветствие с заданным именем.
# Функция sum вычисляет сумму двух аргументов и возвращает результат.

# Определяем функцию greet
greet() {
    echo "Привет, $1!"
}

# Определяем функцию sum
sum() {
    local result=$(( $1 + $2 ))
    echo "$result"
}

# Вызов функции greet с параметром "Мир"
greet "Мир"

# Пример использования функции sum
num1=5
num2=10
result=$(sum $num1 $num2)
echo "Сумма чисел $num1 и $num2 равна $result"
