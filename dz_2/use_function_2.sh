#!/bin/bash
# Пример использования функций в bash
# Скрипт определяет две функции: greet и goodbye.
# Функция greet выводит приветствие, а функция goodbye выводит прощальное сообщение.
# Затем функции вызываются с передачей имени пользователя как аргумента.

greet() {
    echo "Привет, $1!"
}

goodbye() {
    echo "До свидания, $1!"
}

# Вызов функций с аргументом "Иван"
greet "Иван"
goodbye "Иван"
