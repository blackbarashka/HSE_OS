#!/bin/bash
# Этот скрипт запускает цикл while, который запрашивает ввод пользователя.
# Если пользователь вводит "выход", цикл завершается и скрипт заканчивается.
# Иначе, скрипт выводит введённый текст и снова просит пользователя ввести что-либо.

user_input=""
while [ "$user_input" != "выход" ]; do
    echo -n "Введите текст (для выхода введите 'выход'): "
    read user_input
    if [ "$user_input" != "выход" ]; then
        echo "Вы ввели: $user_input"
    fi
done

echo "Скрипт завершён."
